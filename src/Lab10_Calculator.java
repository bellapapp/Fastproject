public class Lab10_Calculator {
    public static void main(String[] args) {
    }

    public int sum(int first, int second){
        return first + second;
    }
    public int subs(int first, int second){
        return first - second;
    }
    public long mul(long first, long second){
        return first * second;
    }
    public float div(float first, float second){
        return first / second;
    }
    public float mod(float first, float second){
        return first % second;
    }

}
